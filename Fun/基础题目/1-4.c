#include <stdio.h>

int main() {
	// 输出正方形
	printf("\t\t* * * * * *\n");
	printf("\t\t*         *\n");
	printf("\t\t*         *\n");
	printf("\t\t*         *\n");
	printf("\t\t*         *\n");
	printf("\t\t* * * * * *\n");

	printf("\n\n");  // 换行

	// 输出长方形
	printf("\t\t      *     \n");
	printf("\t\t    *   *   \n");
	printf("\t\t  *       * \n");
	printf("\t\t* * * * * * *\n");

	return 0;
}